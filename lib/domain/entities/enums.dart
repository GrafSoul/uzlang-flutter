/// Доменные перечисления UzLang.
///
/// Лежат в `domain`, т.к. это бизнес-понятия; на них опираются и слой данных
/// (Drift `intEnum`), и сервисы интервального повторения.
library;

/// Тип обучающей карточки — для полиморфного прогресса и блоков.
enum CardKind {
  /// Отдельное слово.
  word,

  /// Фраза (закрепление из выученных слов).
  phrase,
}

/// Состояние карточки в алгоритме интервального повторения (FSRS).
enum SrState {
  /// Новая, ещё не показывалась.
  newCard,

  /// В процессе первичного заучивания.
  learning,

  /// На долгосрочном повторении.
  review,

  /// Повторное заучивание после провала.
  relearning,
}

/// Оценка ответа пользователя при повторе (рейтинг FSRS).
enum Rating {
  /// Не вспомнил.
  again,

  /// Вспомнил с трудом.
  hard,

  /// Вспомнил нормально.
  good,

  /// Вспомнил легко.
  easy,
}
